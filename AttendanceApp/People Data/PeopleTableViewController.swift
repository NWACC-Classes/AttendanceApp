    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "ContactCell", for: indexPath)
        let contact = contacts[indexPath.row]
        
        cell.textLabel?.text = contact.firstName
        cell.detailTextLabel?.text = contact.phoneNumber
        
        return cell
    }
    
    // Unwind Segue and add contact or just return
    @IBAction func unwindToContactList(segue: UIStoryboardSegue){
        guard let viewController = segue.source as? AddContactsViewController else
        { return }
        
        guard let firstName = viewController.firstNameTextField.text, let lastName = viewController.lastNameTextField.text, let phoneNumber = viewController.phoneNumberTextField.text, let email = viewController.emailTextField.text, let title = viewController.titleTextField.text, let organization = viewController.organizationTextField.text else{ return }
        
        let contact = People(firstName: firstName, lastName: lastName, phoneNumber: phoneNumber, emailAddress: email, organization: organization, title: title)
        contacts.append(contact)
        tableView.reloadData()
        
    }

